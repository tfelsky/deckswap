create unique index if not exists auction_listings_one_open_auction_per_deck_idx
  on public.auction_listings (deck_id)
  where status in ('active', 'pending_confirmation', 'awaiting_payment', 'paid', 'shipped', 'delivered', 'awaiting_settlement', 'under_arbitration');

create index if not exists auction_listings_seller_status_idx
  on public.auction_listings (seller_user_id, status, created_at desc);

create index if not exists auction_listings_status_ends_at_idx
  on public.auction_listings (status, ends_at asc);

create table if not exists public.auction_bids (
  id bigint generated by default as identity primary key,
  auction_id bigint not null references public.auction_listings(id) on delete cascade,
  bidder_user_id uuid not null references auth.users(id) on delete cascade,
  amount_usd numeric not null,
  created_at timestamptz not null default now()
);

create index if not exists auction_bids_listing_created_at_idx
  on public.auction_bids (auction_id, created_at desc);

create index if not exists auction_bids_bidder_created_at_idx
  on public.auction_bids (bidder_user_id, created_at desc);

create table if not exists public.auction_events (
  id bigint generated by default as identity primary key,
  auction_id bigint not null references public.auction_listings(id) on delete cascade,
  actor_user_id uuid references auth.users(id) on delete set null,
  event_type text not null,
  event_data jsonb,
  created_at timestamptz not null default now()
);

create index if not exists auction_events_listing_created_at_idx
  on public.auction_events (auction_id, created_at desc);

create table if not exists public.auction_arbitration_cases (
  id bigint generated by default as identity primary key,
  auction_id bigint not null references public.auction_listings(id) on delete cascade,
  requested_by_user_id uuid not null references auth.users(id) on delete cascade,
  counterparty_user_id uuid references auth.users(id) on delete set null,
  status text not null default 'open',
  issue_type text not null,
  requested_action text,
  claimant_statement text not null,
  respondent_statement text,
  evidence_summary text,
  internal_notes text,
  outcome text,
  last_checkpoint text,
  resolution_deadline_at timestamptz,
  resolved_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (status in ('open', 'in_review', 'resolved', 'closed')),
  check (issue_type in ('non_payment', 'non_delivery', 'item_not_as_described', 'damaged', 'communication_breakdown', 'other'))
);

create index if not exists auction_arbitration_cases_auction_created_at_idx
  on public.auction_arbitration_cases (auction_id, created_at desc);

create index if not exists auction_arbitration_cases_status_created_at_idx
  on public.auction_arbitration_cases (status, created_at desc);

