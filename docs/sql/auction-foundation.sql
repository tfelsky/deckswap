  add column if not exists buyer_payment_marked_at timestamptz,
  add column if not exists seller_fulfillment_marked_at timestamptz,
  add column if not exists buyer_received_marked_at timestamptz,
  add column if not exists settled_at timestamptz,
  add column if not exists dispute_opened_at timestamptz,
  add column if not exists dispute_resolved_at timestamptz,
  add column if not exists dispute_summary text;

