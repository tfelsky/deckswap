create table if not exists public.auction_listings (
  id bigint generated by default as identity primary key,
  deck_id bigint not null references public.decks(id) on delete cascade,
  seller_user_id uuid not null references auth.users(id) on delete cascade,
  status text not null default 'active',
  auction_type text not null default 'reserve',
  settlement_mode text not null default 'managed',
  starting_bid_usd numeric not null default 1,
  reserve_price_usd numeric,
  current_bid_usd numeric not null default 0,
  current_high_bidder_user_id uuid references auth.users(id) on delete set null,
  reserve_met boolean not null default false,
  min_increment_usd numeric not null default 5,
  bid_count integer not null default 0,
  extension_count integer not null default 0,
  duration_days integer not null default 7,
  starts_at timestamptz not null default now(),
  ends_at timestamptz not null,
  winner_user_id uuid references auth.users(id) on delete set null,
  final_bid_usd numeric,
  manual_reviewed_at timestamptz,
  winner_acknowledged_at timestamptz,
  seller_acknowledged_at timestamptz,
  payment_requested_at timestamptz,
  paid_at timestamptz,
  shipped_at timestamptz,
  delivered_at timestamptz,
  payout_released_at timestamptz,
  buyer_payment_marked_at timestamptz,
  seller_fulfillment_marked_at timestamptz,
  buyer_received_marked_at timestamptz,
  settled_at timestamptz,
  dispute_opened_at timestamptz,
  dispute_resolved_at timestamptz,
  dispute_summary text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (status in ('active', 'pending_confirmation', 'awaiting_payment', 'paid', 'shipped', 'delivered', 'payout_released', 'awaiting_settlement', 'under_arbitration', 'completed', 'cancelled', 'expired')),
  check (auction_type in ('reserve', 'no_reserve')),
  check (settlement_mode in ('managed', 'self_cleared')),
  check (duration_days in (3, 5, 7))
);

alter table public.auction_listings
