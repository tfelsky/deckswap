drop policy if exists "auction listings are publicly readable"
  on public.auction_listings;

create policy "auction listings are publicly readable"
  on public.auction_listings
  for select
  using (true);

drop policy if exists "sellers create their own auction listings"
  on public.auction_listings;

create policy "sellers create their own auction listings"
  on public.auction_listings
  for insert
  with check (seller_user_id = auth.uid());

drop policy if exists "auction listings update by participants and admin"
  on public.auction_listings;

create policy "auction listings update by participants and admin"
  on public.auction_listings
  for update
  using (
    seller_user_id = auth.uid()
    or (status = 'active' and auth.uid() is not null and seller_user_id <> auth.uid())
    or auth.jwt() ->> 'email' = 'tim.felsky@gmail.com'
  )
  with check (
    seller_user_id = auth.uid()
    or (status = 'active' and auth.uid() is not null and seller_user_id <> auth.uid())
    or auth.jwt() ->> 'email' = 'tim.felsky@gmail.com'
  );

