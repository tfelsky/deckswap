-- No insert/update policies: all writes go through the security-definer RPCs below,
-- which bypass RLS. This guarantees points can only move through audited code paths.

-- ---------------------------------------------------------------------------
-- RPCs
-- ---------------------------------------------------------------------------

-- Idempotent MP earn. Returns the number of points actually minted (0 if the
-- (user, reason, source) earn already existed). Safe to call from any
-- order-completion path.
create or replace function public.award_reward_points(
  p_user_id     uuid,
  p_amount      integer,
  p_reason      text,
  p_source_type text default null,
  p_source_id   text default null,
  p_usd_basis   numeric default null,
  p_metadata    jsonb default '{}'::jsonb
)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_inserted integer := 0;
begin
  if p_user_id is null then
    raise exception 'A user is required to award reward points.';
  end if;
  if p_amount is null or p_amount <= 0 then
    return 0;
  end if;

  insert into public.reward_point_entries (user_id, amount, reason, source_type, source_id, usd_basis, metadata)
  values (p_user_id, p_amount, p_reason, p_source_type, p_source_id, p_usd_basis, coalesce(p_metadata, '{}'::jsonb))
  on conflict (user_id, reason, source_type, source_id) do nothing;

  get diagnostics v_inserted = row_count;
  return case when v_inserted > 0 then p_amount else 0 end;
end;
$$;

-- Spend MP. Validates the live balance under a row lock and writes a negative
-- entry. Designed to be called from inside a checkout transaction (e.g.
-- create_singles_checkout) so a downstream failure rolls the redemption back.
-- Returns the points actually spent.
create or replace function public.redeem_reward_points(
  p_user_id     uuid,
  p_points      integer,
  p_source_type text,
  p_source_id   text,
  p_min_points  integer default 500
)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_balance integer := 0;
begin
  if p_user_id is null then
    raise exception 'A signed-in member is required to redeem points.';
  end if;
  if p_points is null or p_points <= 0 then
    return 0;
  end if;
  if p_points < coalesce(p_min_points, 0) then
    raise exception 'Redemptions must be at least % points.', p_min_points;
  end if;

  select balance into v_balance
  from public.reward_point_balances
  where user_id = p_user_id
  for update;

  if coalesce(v_balance, 0) < p_points then
    raise exception 'Not enough points: balance % is below requested %.', coalesce(v_balance, 0), p_points;
  end if;

  insert into public.reward_point_entries (user_id, amount, reason, source_type, source_id)
  values (p_user_id, -p_points, 'redemption', p_source_type, p_source_id);

  return p_points;
end;
$$;

-- Idempotent SP earn with an optional same-day cap per reason. Per-entity dedupe
-- comes from the unique constraint; pass a date-bucketed source_id (e.g. the
-- current date) for once-per-day signals. Returns the points actually minted.
create or replace function public.award_engagement_points(
  p_user_id          uuid,
  p_amount           integer,
  p_reason           text,
  p_source_type      text default null,
  p_source_id        text default null,
  p_daily_cap        integer default null,
  p_metadata         jsonb default '{}'::jsonb
)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_awarded_today integer := 0;
  v_grant integer;
  v_inserted integer := 0;
begin
  if p_user_id is null then
    raise exception 'A user is required to award engagement points.';
  end if;
  if p_amount is null or p_amount <= 0 then
    return 0;
  end if;

  v_grant := p_amount;

  if p_daily_cap is not null then
    select coalesce(sum(amount), 0) into v_awarded_today
    from public.engagement_point_entries
    where user_id = p_user_id
      and reason = p_reason
      and amount > 0
      and created_at >= date_trunc('day', now());

    v_grant := least(v_grant, greatest(0, p_daily_cap - v_awarded_today));
    if v_grant <= 0 then
      return 0;
    end if;
  end if;

  insert into public.engagement_point_entries (user_id, amount, reason, source_type, source_id, metadata)
  values (p_user_id, v_grant, p_reason, p_source_type, p_source_id, coalesce(p_metadata, '{}'::jsonb))
  on conflict (user_id, reason, source_type, source_id) do nothing;

  get diagnostics v_inserted = row_count;
  return case when v_inserted > 0 then v_grant else 0 end;
end;
$$;

grant execute on function public.award_reward_points(uuid, integer, text, text, text, numeric, jsonb) to authenticated;
grant execute on function public.redeem_reward_points(uuid, integer, text, text, integer) to authenticated;
grant execute on function public.award_engagement_points(uuid, integer, text, text, text, integer, jsonb) to authenticated;
