-- Mythivex reward + engagement points foundation.
--
-- Two append-only ledgers, walled apart on purpose:
--   * reward_point_entries (MP, "hard" currency) — minted on settled money activity
--     (completed purchases, sales, ticket buys) and redeemable for checkout credit.
--     Carries a usd_basis so earns can be re-derived and audited.
--   * engagement_point_entries (SP / "Spark", soft currency) — minted on signalling
--     activity (listing, add-to-cart, profile completion, daily visits). NEVER
--     redeemable and structurally has no usd_basis. Only ever buys status/visibility.
--
-- Balances are denormalized read models maintained by triggers; the source of truth
-- is always the sum of the ledger. Mutating writes go through security-definer RPCs
-- only, mirroring create_singles_checkout. Admin override is the tim.felsky@gmail.com
-- email, consistent with the rest of the schema.

-- ---------------------------------------------------------------------------
-- Hard currency: Mythivex Points (MP)
-- ---------------------------------------------------------------------------

create table if not exists public.reward_point_entries (
  id          bigint generated by default as identity primary key,
  user_id     uuid not null references auth.users(id) on delete cascade,
  amount      integer not null,              -- +earn / -redeem (whole points)
  reason      text not null,
  source_type text,                          -- 'singles_order','direct_sale_order','event_ticket'
  source_id   text,
  usd_basis   numeric,                       -- the $ figure the earn was computed from
  metadata    jsonb not null default '{}'::jsonb,
  created_at  timestamptz not null default now(),
  constraint reward_point_entries_reason_check check (reason in (
    'singles_purchase', 'singles_sale',
    'deck_purchase', 'deck_sale',
    'ticket_purchase',
    'first_order_bonus',
    'redemption',
    'refund_adjustment', 'manual_adjustment'
  )),
  -- one earn per (user, reason, source): re-running an order-completion path can
  -- never double-mint. Redemptions/adjustments pass a unique source_id (e.g. order id).
  constraint reward_point_entries_unique_source unique (user_id, reason, source_type, source_id)
);

create index if not exists reward_point_entries_user_idx
  on public.reward_point_entries (user_id, created_at desc);

create table if not exists public.reward_point_balances (
  user_id         uuid primary key references auth.users(id) on delete cascade,
  balance         integer not null default 0,
  lifetime_earned integer not null default 0,
  updated_at      timestamptz not null default now()
);

-- ---------------------------------------------------------------------------
-- Soft currency: Spark (SP)
-- ---------------------------------------------------------------------------

create table if not exists public.engagement_point_entries (
  id          bigint generated by default as identity primary key,
  user_id     uuid not null references auth.users(id) on delete cascade,
  amount      integer not null,              -- +earn / -decay (whole points), NO usd basis
  reason      text not null,
  source_type text,                          -- 'single_listing','deck_listing','cart','profile','session'
  source_id   text,
  metadata    jsonb not null default '{}'::jsonb,
  created_at  timestamptz not null default now(),
  constraint engagement_point_entries_reason_check check (reason in (
    'list_item',
    'cart_add', 'cart_add_received',
    'profile_complete',
    'daily_visit',
    'delist_reversal',
    'decay', 'manual_adjustment'
  )),
  constraint engagement_point_entries_unique_source unique (user_id, reason, source_type, source_id)
);

create index if not exists engagement_point_entries_user_idx
  on public.engagement_point_entries (user_id, created_at desc);

create index if not exists engagement_point_entries_daily_cap_idx
  on public.engagement_point_entries (user_id, reason, created_at);

create table if not exists public.engagement_point_balances (
  user_id         uuid primary key references auth.users(id) on delete cascade,
  balance         integer not null default 0,
  lifetime_earned integer not null default 0,
  season_balance  integer not null default 0,
  tier            text not null default 'newcomer',
  updated_at      timestamptz not null default now()
);

-- ---------------------------------------------------------------------------
-- Tier derivation (kept in lockstep with lib/rewards/engagement.ts)
-- ---------------------------------------------------------------------------

create or replace function public.engagement_tier(p_lifetime_earned integer)
returns text
language sql
immutable
as $$
  select case
    when coalesce(p_lifetime_earned, 0) >= 5000 then 'legend'
    when coalesce(p_lifetime_earned, 0) >= 2000 then 'veteran'
    when coalesce(p_lifetime_earned, 0) >= 500  then 'trusted'
    when coalesce(p_lifetime_earned, 0) >= 100  then 'regular'
    else 'newcomer'
  end;
$$;

-- ---------------------------------------------------------------------------
-- Balance maintenance triggers
-- ---------------------------------------------------------------------------

create or replace function public.sync_reward_point_balance()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.reward_point_balances (user_id, balance, lifetime_earned, updated_at)
  values (new.user_id, new.amount, greatest(new.amount, 0), now())
  on conflict (user_id) do update
    set balance = public.reward_point_balances.balance + new.amount,
        lifetime_earned = public.reward_point_balances.lifetime_earned + greatest(new.amount, 0),
        updated_at = now();
  return new;
end;
$$;

drop trigger if exists reward_point_entries_sync_balance on public.reward_point_entries;
create trigger reward_point_entries_sync_balance
  after insert on public.reward_point_entries
  for each row execute function public.sync_reward_point_balance();

create or replace function public.sync_engagement_point_balance()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_lifetime integer;
begin
  insert into public.engagement_point_balances (user_id, balance, lifetime_earned, season_balance, tier, updated_at)
  values (
    new.user_id,
    new.amount,
    greatest(new.amount, 0),
    greatest(new.amount, 0),
    public.engagement_tier(greatest(new.amount, 0)),
    now()
  )
  on conflict (user_id) do update
    set balance = public.engagement_point_balances.balance + new.amount,
        lifetime_earned = public.engagement_point_balances.lifetime_earned + greatest(new.amount, 0),
        season_balance = greatest(0, public.engagement_point_balances.season_balance + new.amount),
        updated_at = now()
  returning lifetime_earned into v_lifetime;

  update public.engagement_point_balances
    set tier = public.engagement_tier(v_lifetime)
    where user_id = new.user_id;

  return new;
end;
$$;

drop trigger if exists engagement_point_entries_sync_balance on public.engagement_point_entries;
create trigger engagement_point_entries_sync_balance
  after insert on public.engagement_point_entries
  for each row execute function public.sync_engagement_point_balance();

